<template>
    <div class="chooser-component">
        <ul class="chooser-list">
          <li v-for='(item,index) in selections'
          :class='{active:checkActive(index)}'>
            <input type="checkbox" :id='checkboxName+"-"+index' v-model='multiplyChoose'
            :value='index'>
            <label :for='checkboxName+"-"+index' @click="chosenSelection(index)">{{item.label}}</label>
          </li>
        </ul>
    </div>
</template>

<script>
import _ from 'lodash'
export default {
  props: {
    selections: {
      type: Array,
      default: [{
        label: 'test',
        value: 0
      }]
    },
    checkboxName:{
      type:String,
      default:'test'
    }
  },
  data () {
    return {
      multiplyChoose: []
    }
  },
  methods: {
    chosenSelection (index) {
      let nowObjArray = _.map(this.multiplyChoose, (idx) => {
        return this.selections[idx]
      })
      this.$emit('on-change', nowObjArray)
      console.log(nowObjArray)
    },
    checkActive (val) {
      return this.multiplyChoose.indexOf(val) !== -1
    }
  }
}
</script>

